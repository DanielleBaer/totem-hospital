    ArrayList<Paciente> lista = new ArrayList<>();
    Paciente paciente = new Paciente();
    
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try{
            paciente.Cpf = txfCpf.getText().toUpperCase();
            if(txfCpf.getText().equals("SAIR")){
                System.exit(0);
            }
            paciente.Nome = txfNome.getText();
            paciente.Idade = Integer.parseInt(txfIdade.getText());
            paciente.Sexo = cbSexo.getSelectedItem().toString();
            alasDeRisco();

            lista.add(paciente);
            
            limpaCampos();
            txfCpf.requestFocus(); 
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Por favor, preencha os campos obrigatórios!");
            txfCpf.requestFocus();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed
    
