    private void limpaCampos(){
        txfCpf.setText("");
        txfNome.setText("");
        txfIdade.setText("");
        cbSexo.setSelectedItem("Selecione");
        ckbContatoCovid.setSelected(false);
        ckbDiarreia.setSelected(false);
        ckbDificuldadeRespiratoria.setSelected(false);
        ckbDorDeCabeca.setSelected(false);
        ckbDorDeGarganta.setSelected(false);
        ckbDorNoCorpo.setSelected(false);
        ckbEspirros.setSelected(false);
        ckbFebre.setSelected(false);
        ckbLocalAglomerado.setSelected(false);
        ckbTosse.setSelected(false);
        
    }
    
    private int verificaCheckComboBox(){
        int cont = 0;
        if(ckbContatoCovid.isSelected()){
            cont = cont + 10;
        }
        if(ckbLocalAglomerado.isSelected()){
            cont = cont + 3;
        }
        if(ckbFebre.isSelected()){
            cont = cont + 5;
        }
        if(ckbDorDeCabeca.isSelected()){
            cont = cont + 1;
        }
        if(ckbEspirros.isSelected()){
            cont = cont + 1;
        }
        if(ckbDorDeGarganta.isSelected()){
            cont = cont + 1;
        }
        if(ckbTosse.isSelected()){
            cont = cont + 3;
        }
        if(ckbDificuldadeRespiratoria.isSelected()){
            cont = cont + 10;
        }
        if(ckbDorNoCorpo.isSelected()){
            cont = cont + 1;
        }
        if(ckbDiarreia.isSelected()){
            cont = cont + 1;
        }  
        return cont;
    }
    
    private void alasDeRisco(){
        int cont = verificaCheckComboBox();
        if(cont < 10){
            JOptionPane.showMessageDialog(null, "Por favor, diriga-se para a ala de risco baixo");
        }else if(cont < 20){
            JOptionPane.showMessageDialog(null, "Por favor, diriga-se para a ala de risco médio");
        }else{
            JOptionPane.showMessageDialog(null, "Por favor, diriga-se para a ala de risco alto");
        }
    }
    
